int	ft_any(char **tab, int(*f)(char *))
{
	int	index;
	int	rst;

	rst = 0;
	index = 0;
	while (tab[index])
	{
		if (f(tab[index]) > 0)
			rst = 1;
		index++;
	}
	return (rst);
}
