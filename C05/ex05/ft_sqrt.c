int	ft_sqrt(int nb)
{
	int	i;

	if (nb == 1)
		return (1);
	i = 1;
	while (i <= 46340 && (i * i) < nb)
		i++;
	if (i * i == nb)
		return (i);
	else
		return (0);
}
